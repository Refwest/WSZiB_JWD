package data_type.Laboratory4;

public class EmployeeData {
    public static void main(String[] args) {
        String firstName;
        String lastName;
        int age;
        char gender;
        long personalId;

        firstName = "Tom";
        lastName = "Smith";
        age = 30;
        gender = 'm';
        personalId = 123456789L;

        System.out.println("First name of employee: " + firstName);
        System.out.println("Last name of employee: " + lastName);
        System.out.println("Age of employee: " + age);
        System.out.println("Gender of employee: " + gender);
        System.out.println("Personal Id of employee: " + personalId);
    }
}
